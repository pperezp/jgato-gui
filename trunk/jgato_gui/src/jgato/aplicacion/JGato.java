    private boolean hayAlgunGanador() {
        if ((tablero[0] == tablero[1] && tablero[1] == tablero[2])
                && (tablero[0] != null && tablero[1] != null && tablero[2] != null)) {
            ponerGato(pos1, pos2, pos3);
            return true;
        } else if ((tablero[3] == tablero[4] && tablero[4] == tablero[5])
                && (tablero[3] != null && tablero[4] != null && tablero[5] != null)) {
            ponerGato(pos4, pos5, pos6);
            return true;
        } else if ((tablero[6] == tablero[7] && tablero[7] == tablero[8])
                && (tablero[6] != null && tablero[7] != null && tablero[8] != null)) {
            ponerGato(pos7, pos8, pos9);
            return true;
        } else if ((tablero[0] == tablero[3] && tablero[3] == tablero[6])
                && (tablero[0] != null && tablero[3] != null && tablero[6] != null)) {
            ponerGato(pos1, pos4, pos7);
            return true;
        } else if ((tablero[1] == tablero[4] && tablero[4] == tablero[7])
                && (tablero[1] != null && tablero[4] != null && tablero[7] != null)) {
            ponerGato(pos2, pos5, pos8);
            return true;
        } else if ((tablero[2] == tablero[5] && tablero[5] == tablero[8])
                && (tablero[2] != null && tablero[5] != null && tablero[8] != null)) {
            ponerGato(pos3, pos6, pos9);
            return true;
        } else if ((tablero[0] == tablero[4] && tablero[4] == tablero[8])
                && (tablero[0] != null && tablero[4] != null && tablero[8] != null)) {
            ponerGato(pos1, pos5, pos9);
            return true;
        } else if ((tablero[2] == tablero[4] && tablero[4] == tablero[6])
                && (tablero[2] != null && tablero[4] != null && tablero[6] != null)) {
            ponerGato(pos3, pos5, pos7);
            return true;
        }
        return false;
    }

