    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try {
            String sr = txtServidor.getText();
            
            cliente = new Socket(sr, 5000);
            
            enviarObjeto(new PeticionFicha());
            
            estado(false);
            
            new Thread(new Runnable() {
                @Override
                public void run() {
                    ObjectInputStream input;
                    while (true) {
                        try {
                            input = new ObjectInputStream(cliente.getInputStream());
                            Object o = input.readObject();
                            if (o instanceof Mensaje) {
                                Mensaje m = (Mensaje) o;
                                jTextArea2.append("\n" + m.getEmisor() + ": " + m.getMensaje());
                            } else if (o instanceof Jugada) {
                                Jugada j = (Jugada) o;
                                setFicha(j.getPosicion(), j.getFicha());
                            } else if(o instanceof RespuestaFicha){
                                RespuestaFicha rf = (RespuestaFicha) o;
                                j2 = new Jugador(2, txtNombreServidor2.getText(), rf.getFicha());
                                
                                yo = j2;
                                
                                enviarObjeto(j2);
                                
                                
                            }else if(o instanceof Jugador){
                                Jugador primerJugador = (Jugador)o;
                                
                                j1 = primerJugador;
                                jFrame1.setVisible(false);
                                JOptionPane.showMessageDialog(null, "Su ficha es: "+j2.getFicha());
                                comenzarPartida();
                            }
                        } catch (IOException ex) {
                            Logger.getLogger(JGato.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (ClassNotFoundException ex) {
                            Logger.getLogger(JGato.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                }
            }).start();
            conectado = true;
        } catch (UnknownHostException ex) {
            Logger.getLogger(JGato.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JGato.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

